package com.szc.common.utils;

public class ExportAttrible {

	private String filedKey;
	private String filedName;
	private int filedType;
	private String multiplyValue;
	private String filedFormat;
	private String extValue;
	
	public String getFiledKey() {
		return filedKey;
	}
	public void setFiledKey(String filedKey) {
		this.filedKey = filedKey;
	}
	public String getFiledName() {
		return filedName;
	}
	public void setFiledName(String filedName) {
		this.filedName = filedName;
	}
	public int getFiledType() {
		return filedType;
	}
	public void setFiledType(int filedType) {
		this.filedType = filedType;
	}
	public String getMultiplyValue() {
		return multiplyValue;
	}
	public void setMultiplyValue(String multiplyValue) {
		this.multiplyValue = multiplyValue;
	}
	public String getFiledFormat() {
		return filedFormat;
	}
	public void setFiledFormat(String filedFormat) {
		this.filedFormat = filedFormat;
	}
	public String getExtValue() {
		return extValue;
	}
	public void setExtValue(String extValue) {
		this.extValue = extValue;
	}
}
